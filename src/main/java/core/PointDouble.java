package core;

public class PointDouble {
    private Double x;
    private Double y;
    
    public PointDouble(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    
    public Double getX() {
        return x;
    }
    
    public void setX(Double x) {
        this.x = x;
    }
    
    public Double getY() {
        return y;
    }
    
    public void setY(Double y) {
        this.y = y;
    }
}
